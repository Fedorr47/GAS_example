void FExtGameplayEffectContext::SetAbilitySource(const IExtAbilitySourceInterface* InObject, float InSourceLevel)
{
	AbilitySourceObject = MakeWeakObjectPtr(Cast<const UObject>(InObject));
	//SourceLevel = InSourceLevel;
}

const IExtAbilitySourceInterface* FExtGameplayEffectContext::GetAbilitySource() const
{
	return Cast<IExtAbilitySourceInterface>(AbilitySourceObject.Get());
}

const UPhysicalMaterial* FExtGameplayEffectContext::GetPhysicalMaterial() const
{
	if (const FHitResult* HitResultPtr = GetHitResult())
	{
		return HitResultPtr->PhysMaterial.Get();
	}
	return nullptr;
}

